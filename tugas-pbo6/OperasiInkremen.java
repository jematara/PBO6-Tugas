public class OperasiInkremen {
    public static void main(String[] args) {
        int x=1;
        System.out.println("Nilai x Awal                    : "+x);
        x++;
        System.out.println("Nilai x Setelah Pasca-increment : "+x);
        ++x;
        System.out.println("Nilai x Setelah Pra-increment   : "+x);
}
}
