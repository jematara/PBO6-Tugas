public class MengubahString {
    public static void main(String[] args) {
        String a="Saya Belajar Java";
        String b="Saya Belajar Java";
        String aLowercase=a.toLowerCase();
        String bUppercase=b.toUpperCase();
        System.out.println(a+" > "+aLowercase+" (lowercase)");
        System.out.println(b+" > "+bUppercase+" (UPPERCASE)");
}
}
